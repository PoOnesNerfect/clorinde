//! Code generation for batch operation trait implementations (QueryExt)
//!
//! This module generates implementations of the `QueryExt` trait for query result types,
//! enabling batch database operations like `insert_many()`, `update_cols()`, etc.
//!
//! The trait implementations are generated based on type annotations in SQL queries
//! (e.g., `--: Restaurant(display_name?, ...)`), which are parsed into `PreparedItem` structs.

use crate::config::Config;
use crate::prepare_queries::{PreparedModule, PreparedItem};
use proc_macro2::{Ident, TokenStream};
use quote::{format_ident, quote};

/// Generate QueryExt trait implementations for all named row types in a module
///
/// # Arguments
/// * `prepared` - The parsed SQL module containing row type definitions
/// * `config` - Configuration specifying whether to generate batch ops and where the trait is defined
///
/// # Returns
/// A TokenStream containing all QueryExt implementations, or None if generation is disabled
pub fn gen_batch_ops_module(
    prepared: &PreparedModule,
    config: &Config,
) -> Option<TokenStream> {
    // Check if batch ops generation is enabled
    if !config.tables.generate_batch_ops {
        return None;
    }

    // Parse the trait module path
    let trait_module = parse_trait_module(&config.tables.trait_module);

    // Generate implementations for each named row type
    let impls: Vec<TokenStream> = prepared
        .rows
        .values()
        .filter(|item| item.is_named) // Only generate for named types
        .filter_map(|item| {
            gen_query_ext_impl(item, &trait_module, config)
        })
        .collect();

    if impls.is_empty() {
        return None;
    }

    // Combine all implementations into a single module
    Some(quote! {
        // Auto-generated QueryExt trait implementations
        // DO NOT EDIT - Generated by Clorinde based on SQL type annotations

        #(#impls)*
    })
}

/// Generate a single QueryExt implementation for a prepared item (row struct)
fn gen_query_ext_impl(
    item: &PreparedItem,
    trait_module: &TokenStream,
    config: &Config,
) -> Option<TokenStream> {
    let struct_name = format_ident!("{}", &item.name.value);
    let field_count = item.fields.len();

    // Determine table name
    let table_name = determine_table_name(item, config)?;

    // Generate COLS array
    let cols = gen_cols_array(item);

    // Generate to_params method
    let to_params = gen_to_params_method(item);

    // Generate from_row method
    let from_row = gen_from_row_method(item);

    Some(quote! {
        impl #trait_module::QueryExt<#field_count> for #struct_name {
            const COLS: [&'static str; #field_count] = #cols;
            const TABLE_NAME: &'static str = #table_name;

            fn to_params(&self) -> [&(dyn tokio_postgres::types::ToSql + Sync); #field_count] {
                #to_params
            }

            fn from_row(row: &tokio_postgres::Row) -> Result<Self, tokio_postgres::Error> {
                #from_row
            }
        }
    })
}

/// Parse trait module path into a TokenStream
///
/// Converts strings like "common::db" into `quote! { common::db }`
fn parse_trait_module(module_path: &str) -> TokenStream {
    let parts: Vec<Ident> = module_path
        .split("::")
        .map(|part| format_ident!("{}", part))
        .collect();

    quote! { #(#parts)::* }
}

/// Determine the SQL table name for a prepared item
///
/// Priority:
/// 1. Explicit mapping in config.tables.table_mapping
/// 2. SQL comment annotation (e.g., `--: Restaurant @table(public.restaurants)`)
/// 3. Inferred from struct name (pluralize + snake_case)
fn determine_table_name(item: &PreparedItem, config: &Config) -> Option<String> {
    let struct_name = &item.name.value;

    // Check explicit mapping
    if let Some(table_name) = config.tables.table_mapping.get(struct_name) {
        return Some(table_name.clone());
    }

    // Check for @table attribute in item attributes
    // TODO: Parse @table(name) from item attributes once implemented

    // Infer from struct name (simple pluralization)
    let inferred = infer_table_name(struct_name);
    Some(inferred)
}

/// Infer SQL table name from Rust struct name
///
/// Simple heuristic: converts PascalCase to snake_case and adds 's' suffix
/// Examples:
/// - Restaurant -> restaurants
/// - RestaurantTag -> restaurant_tags
/// - User -> users
fn infer_table_name(struct_name: &str) -> String {
    let snake_case = to_snake_case(struct_name);
    
    // Simple pluralization: add 's'
    // TODO: Handle irregular plurals (e.g., 'y' -> 'ies')
    format!("{}s", snake_case)
}

/// Convert PascalCase to snake_case
fn to_snake_case(s: &str) -> String {
    let mut result = String::new();
    let mut prev_is_upper = false;

    for (i, ch) in s.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 && !prev_is_upper {
                result.push('_');
            }
            result.push(ch.to_ascii_lowercase());
            prev_is_upper = true;
        } else {
            result.push(ch);
            prev_is_upper = false;
        }
    }

    result
}

/// Generate the COLS array constant
///
/// Example: `["id", "display_name", "location_geog"]`
fn gen_cols_array(item: &PreparedItem) -> TokenStream {
    let col_names: Vec<String> = item
        .fields
        .iter()
        .map(|field| field.ident.db.clone())
        .collect();

    quote! {
        [#(#col_names),*]
    }
}

/// Generate the to_params method implementation
///
/// Converts struct fields into an array of ToSql trait objects
fn gen_to_params_method(item: &PreparedItem) -> TokenStream {
    let field_refs: Vec<TokenStream> = item
        .fields
        .iter()
        .map(|field| {
            let field_name = format_ident!("{}", &field.ident.rs);
            quote! { &self.#field_name }
        })
        .collect();

    quote! {
        [#(#field_refs),*]
    }
}

/// Generate the from_row method implementation
///
/// Extracts values from a database row and constructs the struct
fn gen_from_row_method(item: &PreparedItem) -> TokenStream {
    let field_extractions: Vec<TokenStream> = item
        .fields
        .iter()
        .enumerate()
        .map(|(idx, field)| {
            let field_name = format_ident!("{}", &field.ident.rs);
            let index = proc_macro2::Literal::usize_unsuffixed(idx);
            
            // All fields use try_get (handles both nullable and non-nullable)
            quote! {
                #field_name: row.try_get(#index)?
            }
        })
        .collect();

    quote! {
        Ok(Self {
            #(#field_extractions),*
        })
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_to_snake_case() {
        assert_eq!(to_snake_case("Restaurant"), "restaurant");
        assert_eq!(to_snake_case("RestaurantTag"), "restaurant_tag");
        assert_eq!(to_snake_case("User"), "user");
        assert_eq!(to_snake_case("APIKey"), "a_p_i_key");
    }

    #[test]
    fn test_infer_table_name() {
        assert_eq!(infer_table_name("Restaurant"), "restaurants");
        assert_eq!(infer_table_name("RestaurantTag"), "restaurant_tags");
        assert_eq!(infer_table_name("User"), "users");
    }
}
